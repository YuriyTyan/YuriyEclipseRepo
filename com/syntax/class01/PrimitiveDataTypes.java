package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main (String [] args) {
		/* * Primitive Data Types
		 * 
		 * byte
		 * short
		 * int
		 * long
		 * float
		 * char
		 * boolean
		 */
		
		// Whole Number Data Types
	byte num = 10;
	
	short num1 = 100;
	
	int num2 = 100000;
	
	// l or L
	long num3 = 1223334444555556666L;
	
	// Decimal Number Variables float and double
	// f or F
	
	float num4 = 12.9f;
	
	double num5 = 0.888;
	
	//Non-Number Data Types
	
	char  add = '+';
	char letter ='a';
	
	// true or false (Yes or No) boolean
	// do not put Yes or No for value
	
	boolean hungry = true;
	boolean thursty = false;
	
	
	
	}
}
